    private void btnEliminarCalificacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarCalificacionActionPerformed
        int fila = tblCalificaciones.getSelectedRow();
        if (fila > -1) {
            String materia = tblCalificaciones.getValueAt(fila, 0).toString();
            int unidad = Integer.parseInt(tblCalificaciones.getValueAt(fila, 1).toString());

            int respuesta = JOptionPane.showConfirmDialog(this,
                    "Estás a punto de eliminar la calificación de: " + materia
                    + " de la unidad: " + unidad + ". ¿Deseas continuar?",
                    "Confirmar eliminación",
                    JOptionPane.YES_NO_OPTION);

            if (respuesta == JOptionPane.YES_OPTION) {
                ArchivoCalificaciones.eliminarCalificacion(numControl, materia, unidad);
                JOptionPane.showMessageDialog(this, "Calificación eliminada correctamente.");
                cargarTabla(); 
            }
        } else {
            JOptionPane.showMessageDialog(this, "Selecciona la calificación que deseas eliminar.");
        }

    }//GEN-LAST:event_btnEliminarCalificacionActionPerformed


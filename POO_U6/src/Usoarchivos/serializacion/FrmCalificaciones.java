    private void cargarTabla(){
        ArrayList calificaciones=AccesoArchivo.leerArchivo("calificaciones.txt");
        cargarTabla(calificaciones);
    }
   private void cargarTabla(ArrayList calificaciones) {
    if (calificaciones != null) {
        DefaultTableModel contenidoTabla = (DefaultTableModel) tblCalificaciones.getModel();
        contenidoTabla.setRowCount(0);
        for (Object calificacion : calificaciones) {
            Calificacion a = (Calificacion) calificacion;
            
            // Verifica si el número de control del alumno no es nulo antes de comparar
            if (a.getNumControl() != null && a.getNumControl().getNoControl().equals(this.numControl)) {
                contenidoTabla.addRow(a.toArray());
            }
        }
    }
}




    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        int indiceCarrara = cboCarreras.getSelectedIndex();
        Carrera carreraSeleccionada = carreras.get(indiceCarrara);

        Alumno a = new Alumno(txtNumControl.getText(),
                txtNombre.getText(),
                txtApellidos.getText(),
                //Integer.parseInt(cboSemestre.getSelectedItem().toString())
                cboSemestre.getSelectedIndex() + 1,
                LocalDate.parse(txtFechaNac.getText(), DateTimeFormatter.ofPattern("dd/MM/yyyy")),
                carreraSeleccionada
        );

        if (txtNumControl.isEnabled()) {
            //Agregar
            AccesoArchivo.agregarObjetoArchivo("inscritos.txt", a);
        } else {
            //Editar
            ArrayList<Alumno> alumnos = AccesoArchivo.leerArchivo("inscritos.txt");
            //Buscar la posición del alumno que se modificó
            int posAlumno = alumnos.indexOf(a);
            //Reemplazar el alumno anterior por el nuevo alumno modificado
            alumnos.set(posAlumno, a);
            AccesoArchivo.reescribirArchivo("inscritos.txt", alumnos);
        }
        JOptionPane.showMessageDialog(this, "El alumno ha sido almacenado de manera exitosa");
        FrmListaAlumnos frmLista = new FrmListaAlumnos();
        frmLista.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_btnAceptarActionPerformed


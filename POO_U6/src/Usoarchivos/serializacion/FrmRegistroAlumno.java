    public FrmRegistroAlumno(String numControl) {
        this();

        //Buscar el alumno en el archivo y cargarlo en la GUI
        Alumno alumnoABuscar = new Alumno(numControl);
        ArrayList<Alumno> alumnos = AccesoArchivo.leerArchivo("inscritos.txt");
        int ubicacionAlumno = alumnos.indexOf(alumnoABuscar);
        if (ubicacionAlumno >= 0) {
            //Alumno si existe
            Alumno alumnoEncontrado = alumnos.get(ubicacionAlumno);
            //Remplaza lo de alumno
            txtNumControl.setText(alumnoEncontrado.getNoControl());
            txtNombre.setText(alumnoEncontrado.getNombre());
            txtApellidos.setText(alumnoEncontrado.getApellidos());
            txtFechaNac.setText(alumnoEncontrado.getFechaNac().
                    format(DateTimeFormatter.ofPattern("dd/MM/yyyy")));
            cboSemestre.setSelectedIndex(alumnoEncontrado.getSemestre() - 1);
            cboCarreras.setSelectedItem(alumnoEncontrado.getCarrera().getNombre());
            
            //Esto es para no editar el NumControl desde la interfaz
            txtNumControl.setEnabled(false);
        } else {
            JOptionPane.showMessageDialog(this, "Alumno no encontrado");
        }
    }


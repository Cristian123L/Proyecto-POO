    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
    int indeceMateria = cboMateria.getSelectedIndex();
    int indeceUnidad = cboUnidad.getSelectedIndex();
    
    Materia materiaCapturada = materias.get(indeceMateria);
    
    // Usa getText() en lugar de getName()
    String numControl = lblNumControl.getText();  
    String carrera = lblCarrera.getText();  // Asegúrate de que el texto esté configurado correctamente en la etiqueta
    
    Alumno noControl = new Alumno(numControl);  // Aquí no hay problema en pasar el valor
    Carrera alumnoCarrera = new Carrera(carrera);
    
    // Se captura la calificación desde el JTextField (siempre valida la entrada de usuario)
    int calificacion;
    try {
        calificacion = Integer.parseInt(txtCalificacion.getText());
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "La calificación debe ser un número válido.");
        return;  // Salimos del método si la entrada no es válida
    }

    // Crear la calificación
    Calificacion c = new Calificacion(noControl, alumnoCarrera, materiaCapturada, indeceUnidad, calificacion);
    
    // Guardamos el objeto Calificacion en el archivo
    AccesoArchivo.agregarObjetoArchivo("calificaciones.txt", c);
    
    JOptionPane.showMessageDialog(this, "Calificación capturada correctamente.");
    }//GEN-LAST:event_btnAceptarActionPerformed


    public FrmCapturarCalificacion(String numControl, String nombre, String apellido, String carrera) {
        initComponents();
        this.numControl = numControl;
        this.nombre = nombre;
        this.apellido = apellido;
        this.carrera = carrera;

        String fullName = nombre + " " + apellido;

        lblAlumno.setText(fullName);
        lblCarrera.setText(carrera);
        lblNumControl.setText(numControl);

//        Cargar materias
        DefaultComboBoxModel modelo = new DefaultComboBoxModel();
        materias = AccesoArchivo.leerArchivo("materias.txt");
        
        for (Materia materia : materias) {
            modelo.addElement(materia.getNombre());
        }
        cboMateria.setModel(modelo);
        
//        Crear unidades
        for (int i = 1; i <= 12; i++) {
            cboUnidad.addItem(String.valueOf(i));
        }
    }


